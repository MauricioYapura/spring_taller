package spring.proyect.model;

public class CDicta {
	public int codp;
	public String nombre;
	public int	codg;
	public String grupo;
	public int  gestion;
	public int estado;
	public int getCodp() {
		return codp;
	}
	public void setCodp(int codp) {
		this.codp = codp;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getCodg() {
		return codg;
	}
	public void setCodg(int codg) {
		this.codg = codg;
	}
	public String getGrupo() {
		return grupo;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	public int getGestion() {
		return gestion;
	}
	public void setGestion(int gestion) {
		this.gestion = gestion;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}

}
