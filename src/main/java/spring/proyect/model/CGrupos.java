package spring.proyect.model;

public class CGrupos {
	public int codg;
	public String nombre;
	public  int estado;

	
	
	public int getCodg() {
		return codg;
	}
	public void setCodg(int codg) {
		this.codg = codg;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	
	

}
